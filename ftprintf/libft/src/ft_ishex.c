#include <libft.h>

int		ft_ishex(char c)
{
	if (ft_isdigit(c))
		return (1);
	if ((c >= 'a') && (c <= 'f'))
		return (1);
	if ((c >= 'A') && (c <= 'F'))
		return (1);
	return (0);
}
