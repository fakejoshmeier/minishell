#include <libft.h>

void	ft_strtoupper(char **s)
{
	int	i;

	i = 0;
	if (!s || !*s)
		return ;
	while (s[0][i])
	{
		if (s[0][i] > 96 && s[0][i] < 123)
			s[0][i] -= 32;
		i++;
	}
	return ;
}
