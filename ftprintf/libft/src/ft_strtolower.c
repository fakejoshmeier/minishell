#include <libft.h>

void	ft_strtolower(char **s)
{
	int	i;

	i = 0;
	if (!s || !*s)
		return ;
	while (s[0][i])
	{
		if (s[0][i] >= 'A' && s[0][i] <= 'Z')
			s[0][i] += 32;
		i++;
	}
	return ;
}
